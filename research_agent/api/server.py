@app.post("/api/upload")
async def upload_document(file: UploadFile = File(...), session_id: str = Form(default="")):
    sid = session_id or str(uuid.uuid4())
    try:
        raw = await file.read()
        doc_id = f"doc_{uuid.uuid4().hex[:8]}"

        await manager.send(sid, {"type": "upload_started", "file_name": file.filename, "agent": "DataProcessingAgent"})

        result = store.add_document(doc_id, file.filename, raw)

        await manager.send(sid, {"type": "agent_step", "agent": "DataProcessingAgent", "step": "chunk", "progress": 60, "result": result})
        await manager.send(sid, {"type": "agent_step", "agent": "KnowledgeGraphAgent", "step": "extract_knowledge", "progress": 100, "result": result})
        await manager.send(sid, {"type": "upload_complete", **result})

        return {
            "status": "success", "file_name": file.filename, "doc_id": doc_id,
            "file_size": len(raw), "chunks": result["num_chunks"],
            "entities": result["entities_extracted"], "relations": result["relations_extracted"],
            "word_count": result["word_count"], "session_id": sid,
            "llm_ready": groq_client is not None,
            "message": f"'{file.filename}' indexed — {result['word_count']:,} words in {result['num_chunks']} chunks",
        }
    except Exception as e:
        logger.error(f"Upload error: {e}", exc_info=True)
        raise HTTPException(status_code=500, detail=str(e))

# ── Query Pipeline ─────────────────────────────────────────────────────────────

@app.post("/api/query")
async def research_query(request: QueryRequest):
    """Run the full 6-agent research pipeline. Answers come from the real uploaded paper via LLM."""
    sid   = request.session_id or str(uuid.uuid4())
    query = request.query.strip()

    try:
        # ── Step 1-2: Retrieve relevant chunks ──
        await manager.send(sid, {"type": "agent_active", "agent": "LiteratureReviewAgent"})
        matched_chunks = store.search(query, top_k=8)
        await manager.send(sid, {"type": "agent_step", "agent": "LiteratureReviewAgent", "progress": 20})

        # ── Step 3: Knowledge graph ──
        await manager.send(sid, {"type": "agent_active", "agent": "DataProcessingAgent"})
        await manager.send(sid, {"type": "agent_step", "agent": "DataProcessingAgent", "progress": 38})

        await manager.send(sid, {"type": "agent_active", "agent": "KnowledgeGraphAgent"})
        entities  = store.all_entities()
        relations = store.all_relations()
        await manager.send(sid, {"type": "agent_step", "agent": "KnowledgeGraphAgent", "progress": 58})

        # ── Step 4: Analysis ──
        await manager.send(sid, {"type": "agent_active", "agent": "AnalysisAgent"})
        await manager.send(sid, {"type": "agent_step", "agent": "AnalysisAgent", "progress": 74})

        # ── Step 5: Generate answer via LLM ──
        await manager.send(sid, {"type": "agent_active", "agent": "WritingAssistantAgent"})
        # Run LLM call in thread so it doesn't block the event loop
        answer = await asyncio.get_event_loop().run_in_executor(
            None, _llm_answer, query, matched_chunks
        )
        await manager.send(sid, {"type": "agent_step", "agent": "WritingAssistantAgent", "progress": 92})

        # ── Step 6: Log ──
        await collaboration_agent.execute({
            "action": "log_action", "action_type": "research_query",
            "agent": "Pipeline", "details": {"query": query, "session_id": sid},
        })
        await manager.send(sid, {"type": "pipeline_complete", "progress": 100})

        sources   = _sources_from_chunks(matched_chunks)
        citations = _build_citations(entities)

        return {
            "status": "success", "query": query, "answer": answer,
            "citations": citations,
            "confidence": round(0.75 + min(len(matched_chunks) * 0.025, 0.2), 2),
            "sources": sources,
            "agent_steps": [
                {"agent": "LiteratureReviewAgent", "action": "retrieve_chunks", "status": "completed"},
                {"agent": "DataProcessingAgent",   "action": "filter",          "status": "completed"},
                {"agent": "KnowledgeGraphAgent",   "action": "query_graph",     "status": "completed"},
                {"agent": "AnalysisAgent",         "action": "analyze_trends",  "status": "completed"},
                {"agent": "WritingAssistantAgent", "action": "generate_answer", "status": "completed"},
                {"agent": "CollaborationAgent",    "action": "log_action",      "status": "completed"},
            ],
            "pipeline_used": list(AGENTS.keys()),
            "llm_used": groq_client is not None,
            "session_id": sid,
        }

    except Exception as e:
        logger.error(f"Query error: {e}", exc_info=True)
        raise HTTPException(status_code=500, detail=str(e))

# ── Individual agent ──────────────────────────────────────────────────────────

@app.post("/api/agent/execute")
async def execute_agent(request: AgentActionRequest):
    if request.agent not in AGENTS:
        raise HTTPException(status_code=404, detail=f"Agent '{request.agent}' not found")
    result = await AGENTS[request.agent].execute({"action": request.action, **request.params})
    if request.session_id:
        await manager.send(request.session_id, {"type": "agent_result", "agent": request.agent, "result": result})
    return result

@app.get("/api/agent/{agent_name}/status")
async def agent_status(agent_name: str):
    if agent_name not in AGENTS:
        raise HTTPException(status_code=404)
    a = AGENTS[agent_name]
    return {"agent": agent_name, "status": a.status, "description": a.description}

