# ── LLM Answer Generation ─────────────────────────────────────────────────────

def _build_context(query: str, chunks: List[Dict]) -> str:
    """Build a clean context string from the most relevant chunks."""
    if not chunks:
        full = store.full_text()
        return full[:8000] if full else ""

    # Use all top chunks joined — don't truncate sentences mid-way
    parts = []
    total = 0
    for ch in chunks[:6]:
        text = ch["text"].strip()
        if total + len(text) > 7000:
            break
        parts.append(text)
        total += len(text)
    return "\n\n".join(parts)


def _llm_answer(query: str, context_chunks: List[Dict]) -> str:
    """
    Generate an answer using Groq LLM, or fall back to extractive answer.
    """
    if not groq_client:
        return _extractive_answer(query, context_chunks)

    context_text = _build_context(query, context_chunks)
    if not context_text:
        return "⚠️ No documents uploaded yet. Please upload a research paper first."

    system_prompt = (
        "You are a helpful research assistant. The user has uploaded a research document "
        "and will ask questions about it. Your job is to answer clearly and simply.\n\n"
        "Rules:\n"
        "- Answer in plain, easy-to-understand English\n"
        "- Be direct — start with the answer, then add detail\n"
        "- Use bullet points when listing multiple items\n"
        "- Keep it concise but complete — aim for 3-6 sentences or bullet points\n"
        "- Only use information from the provided document text\n"
        "- If the document doesn't contain the answer, say so clearly\n"
        "- Never start with 'Based on the excerpts' or 'According to the document'"
    )

    user_prompt = (
        f"Document content:\n\n{context_text}\n\n"
        f"Question: {query}\n\n"
        "Answer (simple and clear):"
    )

    try:
        response = groq_client.chat.completions.create(
            model="llama-3.3-70b-versatile",
            messages=[
                {"role": "system", "content": system_prompt},
                {"role": "user",   "content": user_prompt},
            ],
            temperature=0.2,
            max_tokens=800,
        )
        return response.choices[0].message.content.strip()
    except Exception as e:
        logger.error(f"Groq API error: {e}")
        return _extractive_answer(query, context_chunks)


def _extractive_answer(query: str, chunks: List[Dict]) -> str:
    """
    Fallback: extract and present the most relevant sentences from the paper
    in a clean, readable way — even without an LLM.
    """
    if store.is_empty:
        return "⚠️ No documents uploaded yet. Please upload a research paper first."

    doc_name = next(iter(store._docs.values()), {}).get("filename", "uploaded document")

    # Get top relevant sentences
    sentences = store.search_sentences(query, top_k=8)
    entities  = store.all_entities()
    q_lower   = query.lower()

    lines: List[str] = []

    # ── Knowledge graph question ─────────────────────────────────────────────
    if any(w in q_lower for w in ["knowledge graph", "entities", "relations", "graph"]):
        methods  = [e["name"] for e in entities if e["type"] == "method"][:12]
        concepts = [e["name"] for e in entities if e["type"] == "concept"][:8]
        authors  = [e["name"] for e in entities if e["type"] == "author"][:6]
        relations = store.all_relations()[:10]

        lines.append(f"**Knowledge Graph — {doc_name}**\n")
        if methods:
            lines.append("**Methods & Models:**")
            lines.extend(f"- {m}" for m in methods)
        if concepts:
            lines.append(f"\n**Key Concepts:** {', '.join(concepts)}")
        if authors:
            years = [e["name"] for e in entities if e["type"] == "year"]
            lines.append("\n**Authors:**")
            for i, a in enumerate(authors):
                yr = years[i] if i < len(years) else ""
                lines.append(f"- {a}" + (f" ({yr})" if yr else ""))
        if relations:
            lines.append("\n**Connections:**")
            for r in relations[:6]:
                lines.append(f"- {r['source']} → {r['target']}")
        return "\n".join(lines)

    # ── General question: show relevant sentences ────────────────────────────
    lines.append(f"**From the paper: *{doc_name}***\n")

    if sentences:
        lines.append("Here is what the paper says about this:\n")
        for s in sentences[:6]:
            # Clean up the sentence
            text = s["text"].strip()
            text = re.sub(r"\s+", " ", text)
            lines.append(f"• {text}")
    else:
        # No strong keyword match — show opening of the paper
        all_chunks = store.all_chunks()
        if all_chunks:
            intro = all_chunks[0]["text"][:800].strip()
            intro = re.sub(r"\s+", " ", intro)
            lines.append(f"{intro}")
        else:
            lines.append("The document has been uploaded but could not be read properly.")

    # Add a note about configuring LLM for better answers
    lines.append(f"\n---\n_For more natural AI-generated answers, add your `GROQ_API_KEY` to `e:\\pcl\\research_agent\\.env`_")

    return "\n".join(lines)



def _build_citations(entities: List[Dict]) -> List[str]:
    authors = [e["name"] for e in entities if e["type"] == "author"]
    years   = [e["name"] for e in entities if e["type"] == "year"]
    return [f"{a} ({years[i] if i < len(years) else 'n.d.'})" for i, a in enumerate(authors[:6])]


def _sources_from_chunks(chunks: List[Dict]) -> List[Dict]:
    sources, seen = [], set()
    for ch in chunks[:5]:
        doc_id = ch["doc_id"]
        if doc_id in seen:
            continue
        seen.add(doc_id)
        doc = store.get_document(doc_id) or {}
        ents = doc.get("entities", [])
        authors = [e["name"] for e in ents if e["type"] == "author"][:3]
        years   = [e["name"] for e in ents if e["type"] == "year"]
        sources.append({
            "title":           ch["filename"],
            "authors":         authors or ["Uploaded Document"],
            "year":            int(years[0]) if years else 2024,
            "venue":           "Uploaded Document",
            "relevance_score": ch.get("relevance_score", 0.9),
            "excerpt":         ch["text"][:200],
        })
    return sources

